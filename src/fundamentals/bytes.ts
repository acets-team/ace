/** 1 byte */
export const byte = 1

/** Number of bytes in a kilobyte (KB) */
export const bytesKB = byte * 1024

/** Number of bytes in a megabyte (MB) */
export const bytesMB = bytesKB * 1024

/** Number of bytes in a gigabyte (GB) */
export const bytesGB = bytesMB * 1024

/** Number of bytes in a terabyte (TB) */
export const bytesTB = bytesGB * 1024

/** Number of bytes in a petabyte (PB) */
export const bytesPB = bytesTB * 1024
